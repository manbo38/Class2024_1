package p2024_07_02;

public class Oper01 {

	public static void main(String[] args) {
//		산술 연산자 : +, -, *, /(몫), %(나머지)
		int a=10, b=3, c;
		
		c = a + b;
		System.out.println("a+b="+c);
		System.out.println("a+b="+(a+b));
		System.out.println("a-b="+(a-b));
		System.out.println("a*b="+(a*b));
		System.out.println("a/b="+(a/b));
		System.out.println("a%b="+(a%b));

	}

}
